function slugify(input: string) {
  return input
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/(^-|-$)/g, "");
}

function categorize(code: string) {
  const prefix = code.slice(0, 2);
  // coarse mapping based on occupation groups - safe default categories for filtering
  switch (prefix) {
    case "11":
    case "13":
      return "Management";
    case "12":
    case "31":
    case "32":
      return "Agriculture & Technical";
    case "14":
    case "35":
    case "45":
      return "Hospitality";
    case "21":
      return "Media & Arts";
    case "22":
    case "23":
      return "Business & Finance";
    case "23":
      return "Engineering";
    case "24":
      return "Education";
    case "25":
    case "26":
      return "Healthcare";
    case "26":
      return "ICT";
    case "27":
      return "Legal & Community";
    case "33":
    case "34":
    case "39":
      return "Trades & Construction";
    case "36":
      return "Horticulture & Animal Care";
    case "41":
    case "42":
      return "Community & Care";
    case "43":
    case "51":
    case "52":
      return "Administration";
    case "59":
    case "61":
      return "Legal & Insurance";
    case "63":
      return "Retail";
    default:
      // last-resort mapping based on first digit
      const first = code.charAt(0);
      if (first === "2") return "Professional Services";
      if (first === "3" || first === "4") return "Trades & Services";
      return "Other";
  }
}

